void Cube::Draw()
{
	if (m_VAO == 0)
	{
		Init();
	}

	glBindVertexArray(m_VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}


void Cube::DrawInstanced(unsigned int count)
{
	if (m_VAO == 0)
	{
		Init();
	}

	glBindVertexArray(m_VAO);
	glDrawArraysInstanced(GL_TRIANGLE_STRIP, 0, 36, count);
	glBindVertexArray(0);
}
